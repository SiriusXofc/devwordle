export function getWordHint(word: string) {
  const normalized = word.toUpperCase();
  return (
    WORD_HINTS[normalized] ?? {
      category: WORD_CATEGORIES[normalized] ?? "tech",
      hint: `termo de ${getWordDifficulty(normalized)} usado em desenvolvimento`,
    }
  );
}

export function getWordCategory(word: string) {
  return getWordHint(word).category;
}
