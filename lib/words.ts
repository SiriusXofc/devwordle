function cleanWords(words: readonly string[]) {
  return Array.from(new Set(words.filter((word) => /^[A-Z]{5}$/.test(word))));
}

export const EASY_WORD_POOL = cleanWords(EASY_WORDS);
export const MEDIUM_WORD_POOL = cleanWords(MEDIUM_WORDS);
export const HARD_WORD_POOL = cleanWords(HARD_ONLY_WORDS);
export const DAILY_WORD_POOL = cleanWords(DAILY_WORDS);

export const WORDS = cleanWords([...EASY_WORD_POOL, ...MEDIUM_WORD_POOL, ...HARD_WORD_POOL]);

const EASY_SET = new Set(EASY_WORD_POOL);
const MEDIUM_SET = new Set(MEDIUM_WORD_POOL);

export function getWordDifficulty(word: string): WordDifficulty {
  const normalized = word.toUpperCase();
  if (EASY_SET.has(normalized)) {
    return "easy";
  }
  if (MEDIUM_SET.has(normalized)) {
    return "medium";
  }
  return "hard";
}

export function getWordsByMode(mode: GameMode) {
  switch (mode) {
    case "CLASSIC":
    case "SPEED":
      return cleanWords([...EASY_WORD_POOL, ...MEDIUM_WORD_POOL]);
    case "HARD":
      return cleanWords([...MEDIUM_WORD_POOL, ...HARD_WORD_POOL]);
    case "DAILY":
      return DAILY_WORD_POOL;
    default:
      return cleanWords([...EASY_WORD_POOL, ...MEDIUM_WORD_POOL]);
  }
}

